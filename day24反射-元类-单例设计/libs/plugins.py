
class WinCmd:
    def cd(self):
        print("windows进入到某个目录")

    def delete(self):
        print("windows删除了目录")

    def tasklist(self):
        print("windows 打印了当前任务")

class LinuxCmd:
    def ls(self):
        print("Linux列出了目录")

    def rm(self):
        print("Linux删除了目录")

    def ps(self):
        print("Linux 打印了当前任务")